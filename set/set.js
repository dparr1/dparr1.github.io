function createSeededRandom(seed) {
  let state = seed % 781200; // Ensure seed is within a reasonable range

  return function() {
    state = (state * 16807) % 781200; // LCG algorithm
    return state / 781200; // Normalize to a value between 0 and 1
  };
}

function shuffleDeck() {
    for (var idx = 0; idx < deck.length; idx++) {
        swapIdx = idx + Math.floor(Math.random() * (deck.length - idx));
        var tmp = deck[idx];
        deck[idx] = deck[swapIdx];
        deck[swapIdx] = tmp;
    }
}

